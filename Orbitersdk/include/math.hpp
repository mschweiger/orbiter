#define MATH_HPP

////////////////////////////////////////////////////////////////////////////////
/**
 * @brief return x saturated to the range [0, 1]
*/
template<typename T>
constexpr auto saturate(T x) { return x < T{0} ? T{0} : (x > T{1} ? T{1} : x); }

